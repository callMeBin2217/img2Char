from PIL import Image
import argparse

class img2Char():
	def __init__(self):
		self.IMG = r'C:/Users/LENOVO/Desktop/img1/b.jpg'
		self.WIDTH =100
		self.HEIGHT =50


		self.ascii_char = list(r"$@B%8&WM#*oahkbdpqwmZO0QLCJUYXzcvunxrjft/\|()1{}[]?-_+~<>i!;:,\"^`.")


	def get_char(self,r,g,b,alpha=256):
		if alpha==0:
			return ' '
		length = len(self.ascii_char)
		gray = int(0.2126*r+0.7152*g+0.0722*b)
		unit = (256.0+1)/length
		return self.ascii_char[int(gray/unit)]


	def save2File(self):
		im = Image.open(self.IMG)
		#scale = max(im.size[0]/self.WIDTH,im.size[1]/self.HEIGHT)
		#self.WIDTH = im.size[0]/scale *2
		#self.HEIGHT = im.size[1]/scale
		im = im.resize((self.WIDTH,self.HEIGHT),Image.NEAREST)
		txt = ""

		for i in range(self.HEIGHT):
			for j in range(self.WIDTH):
				txt +=self.get_char(*im.getpixel((j,i))[:3])
			txt+='\n'

		with open(r'C:/Users/LENOVO/Desktop/img1/output.txt','w') as f:
			f.write(txt)


img2char = img2Char()
img2char.save2File()

